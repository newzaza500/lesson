        <div class="container4">
            <h5>ปัญหาสิ่งแวดล้อมจากภาคคมนาคมขนส่ง</h5>
            <p>ปัญหาและผลกระทบจากการคมนาคมขนส่งมาจากก๊าซเสีย ไอเสีย และฝุ่นละอองที่ปล่อยออกมา จากเครื่องยนต์ ก๊าซเสียบางส่วน และฝุ่นละอองที่เกิดขึ้น เป็นผลมาจากการเผาไหม้ที่ไม่สมบูรณ์ โดยทั่วไปเขม่าฝุ่นละอองมีขนาดเล็กมาก (เล็กกว่า 10 ไมครอน หรือ PM 10) เมื่อสูดลมหายใจเข้าไป จะเป็นอันตรายต่อปอดของมนุษย์ซึ่งมีผลต่อชีวิตและสุขภาพ อันตรายมากสำหรับชุมชนเมืองใหญ่ที่มีการจราจรติดขัดมากๆ ปัญหาดังกล่าวมีแนวโน้มจะเป็นการเกิดรูรั่วของชั้นโอโซน (Ozone Depletion) รูรั่วในชั้นโอโซนเกิดขึ้นเนื่องจากสาร คลอโรฟลูโอโรคาร์บอน (CFC) ซึ่งเป็นสารที่ใช้ในการทำความเย็น เช่น ใช้ในการทำตู้เย็น เครื่องปรับอากาศและในการอัดความดันในกระป๋องสเปรย์ต่างๆ และสารฮาลอน (Halon) สารจำพวกนี้เมื่อปล่อยออกไป ในบรรยากาศจะลอยขึ้นไปยังชั้นโทรโพสเฟียร์ และชั้นสแตรโทสเฟียร์ ซึ่งในบรรยากาศชั้นนี้จะมี ก๊าซโอโซน (O) ซึ่งทำหน้าที่เป็นตัวกั้นรังสีอัลตราไวโอเลตบี (UVB) ที่แผ่กระจายมายังโลกไม่ให้มีปริมาณมากเกินไปจนเป็นอันตรายต่อมนุษย์ได้ โดยปกติถ้าผิวหนังมนุษย์ได้รับรังสี UVB มากเกินไปอาจทำให้</p><br><br>
            <p>เกิดโรคมะเร็งผิวหนังได้ เมื่อสารจำพวก CFC ปะปนอยู่ในชั้นสแตรโทสเฟียร์ รังสีอัลตราไวโอเลตจะ กระตุ้นให้อะตอมคลอรีนแตกตัวออกมาจากสาร CFC ซึ่งอะตอมคลอรีนนี้จะจับตัวกับออกซิเจนอะตอม ในโอโซนทำให้โอโซนถูกทำลายลง</p>
          
            <p>เนื่องจากก๊าซโอโซนเป็นตัวกรองรังสี UVB ซึ่งเป็นรังสีที่เป็นอันตรายต่อผิวหนังมนุษย์ ยิ่งก๊าซ โอโซนถูกทำลายมากเท่าไร ก็แสดงว่าสิ่งมีชีวิตบนโลกจะได้รับความเสี่ยงจากอันตรายของรังสี UVB มากขึ้น ดังนั้นปัญหาการเกิดรูรั่วของชั้นโอโซนนี้ จำเป็นต้องมีการป้องกันโดยการลดการใช้สาร CFC</p>
          
            <p><b>ฝนกรด (Acid Rain)</b>คือ ฝนหรือหิมะที่ตกลงมา โดยมีสภาพเป็นกรดจากสารซัลเฟอร์ไดออกไซด์ สารนี้เกิดจากการเผาไหม้ถ่านหินที่มีส่วนประกอบของกำมะถัน (ซัลเฟอร์) จากโรงไฟฟ้าและสาร ไนโตรเจนออกไซด์ ซึ่งเกิดจากการเผาไหม้เชื้อเพลิง โดยเฉพาะการเผาไหม้เชื้อเพลิงในรถยนต์ เมื่อ ซัลเฟอร์ไดออกไซด์ และไนโตรเจนออกไซด์ลอยขึ้น สู่ อากาศก็จะรวมตัวกับไอน้ำกลายเป็นกรดซัลฟูริกและกรดไนตริก ตกลงมาเป็นฝนที่มีสภาพเป็นกรดเป็นอันตรายต่อโลหะ สิ่งก่อสร้าง พืช และมนุษย์</p><br>
            <p><b>สภาวะโลกร้อน (Global Warming)</b>เป็นสภาวะที่มีก๊าซบางชนิดสะสมอยู่ในบรรยากาศโลกเป็นจำนวนมาก ทำให้รังสีความร้อนที่แผ่จากโลกไม่สามารถกระจายออกไปนอกบรรยากาศได้ทำให้สภาพภูมิอากาศโดยรวมของโลกมีความร้อนสะสมอยู่มากเป็นผลให้อุณหภูมิของอากาศในโลกสูงขึ้นเรื่อยๆ ปรากฏการณ์นี้มี ลักษณะเช่นเดียวกับการรักษาความร้อนภายในเรือนเพาะชำกระจก (GreenHouse) ทางการเกษตร</p>
