            <p>การผลิต และการใช้พลังงานในโรงงานก่อให้เกิดมลพิษ และของเสียปลดปล่อยออกมา ซึ่งอาจส่งผลกระทบต่อสังคม และชุมชนที่อาศัยอยู่ใกล้เคียงกับโรงงาน สรุปได้ดังนี้</p><br><br>
            <p><b>น้ำทิ้ง</b>น้ำทิ้งจากโรงงานมีทั้งน้ำที่ระบายทิ้งจากหม้อไอน้ำ หอหล่อเย็น (Cooling Tower) น้ำเสียจากกระบวนการผลิต และระบบการหล่อเย็นอื่นๆ น้ำดังกล่าวจะมีสิ่งสกปรก มลพิษสารแขวนลอย หรือของเสียปะปนอยู่อาจก่อให้เกิดผลกระทบต่อสิ่งแวดล้อม</p><br>
            <p><b>ขยะหรือกากของเสีย</b>ขยะจากโรงงานอุตสาหกรรมมีทั้งขยะที่มีอันตรายและไม่มีอันตราย ซึ่งจะ ต้องมีการกำจัดโดยกระบวนการเผา แยกขยะนำกลับมาใช้ใหม่ หรือบำบัดความเป็นพิษของกากของเสียนั้น ก่อนนำไปสู่กระบวนการกำจัดปกติหรือการฝังกลบในพื้นดิน</p><br>
            <p><b>มลพิษทางอากาศ</b>อากาศที่ปล่อยทิ้งจากปล่องโรงงานอุตสาหกรรมส่วนใหญ่เป็นก๊าซที่เกิด จากการเผาไหม้เชื้อเพลิงในกระบวนการผลิต ก๊าซเหล่านี้ส่วนมากเป็นก๊าซเรือนกระจก ซึ่งได้แก่ ก๊าซคาร์บอนไดออกไซด์ ก๊าซไนโตรเจน รวมทั้งฝุ่นละอองที่เป็นขี้เถ้า</p><br>
