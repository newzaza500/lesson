        <h5>ก๊าซเรือนกระจก</h5>
        <br>
        <p>ก๊าซเรือนกระจกที่ทำให้เกิดสภาวะโลกร้อน มีดังนี้</p><br>
        <br>
        <p><b>ก๊าซคาร์บอนไดออกไซด์</b> (Carbon Dioxide: CO₂) เป็นก๊าซที่ไม่มีกลิ่น ใช้อัดลงในน้ำดื่มเพื่อให้เกิดฟองซ่า เช่น โซดา น้ำอัดลม และยังนำไปใช้ในอุตสาหกรรมอื่นๆ อีกหลายประเภท คาร์บอนไดออกไซด์นับเป็นตัวการสำคัญที่สุดที่ทำให้เกิดสภาวะโลกร้อน โดยมีสัดส่วนถึงร้อยละ 76 เกิดจากการเผาไหม้เชื้อเพลิงชนิดต่างๆ เช่น น้ำมัน ถ่านหิน และก๊าซธรรมชาติ (ไม่ใช่ "หรือฟิน" ซึ่งไม่มีความหมายในบริบทนี้) เชื้อเพลิงเหล่านี้เมื่อเผาแล้วจะได้ก๊าซคาร์บอนไดออกไซด์ ซึ่งเป็นก๊าซที่ทำให้เกิดพลังงานความร้อนและสะสมอยู่ในบรรยากาศของโลกมากที่สุด ก๊าซชนิดนี้ส่วนใหญ่เกิดจากกิจกรรมต่างๆ ของมนุษย์ ได้แก่ การเผาไหม้เชื้อเพลิงในกระบวนการอุตสาหกรรม ในยานพาหนะ การผลิตพลังงานไฟฟ้า และการเผาป่า เป็นต้น</p>
        <br>
        <p>ปัจจุบัน ก๊าซคาร์บอนไดออกไซด์ถูกปล่อยเข้าสู่บรรยากาศโลกรวมกว่าปีละ 30,000 ล้านตัน ซึ่งเป็นผลมาจากการเร่งรัดพัฒนาเศรษฐกิจ ระหว่างปี ค.ศ. 1990-2011 ก๊าซคาร์บอนไดออกไซด์มีบทบาททำให้อุณหภูมิโลกสูงขึ้นถึงร้อยละ 30</p>
        <br>
        <p><b>ก๊าซมีเทน</b> (Methane: CH₄) เป็นก๊าซเรือนกระจกที่สำคัญอันดับสอง เกิดขึ้นเองตามธรรมชาติจากการย่อยสลายสารอินทรีย์โดยแบคทีเรียในสภาพที่ปราศจากออกซิเจน หรือการหมักในสภาพไร้อากาศ ได้แก่ แปลงเพาะปลูกต่างๆ นาข้าวที่มีน้ำขัง การย่อยสลายของฟางในนาข้าว ระบบบำบัดน้ำเสียชนิดไร้อากาศ การฝังกลบขยะ การทำเหมืองถ่านหิน และการผลิตก๊าซธรรมชาติ เป็นต้น คาดว่าในปี ค.ศ. 2050 ปริมาณก๊าซนี้จะเพิ่มขึ้นเป็น 2 เท่าของปริมาณในปัจจุบันก๊าซมีเทนมีอายุคงอยู่ในบรรยากาศได้ประมาณ 10 ปี และมีศักยภาพในการทำให้เกิดสภาวะโลกร้อนได้มากกว่าก๊าซคาร์บอนไดออกไซด์ถึง 20 เท่า</p>
        <br>
        <p><b>ก๊าซไนตรัสออกไซด์</b> (Nitrous Oxide: N₂O) หรือ ก๊าซหัวเราะ (Laughing Gas) เป็นก๊าซที่ถูกปล่อยสู่บรรยากาศจากแหล่งธรรมชาติ รวมถึง มหาสมุทร ดิน การสันดาปน้ำมันเชื้อเพลิง การเผาไหม้มวลชีวภาพ การใช้ปุ๋ย และอุตสาหกรรมต่างๆก๊าซชนิดนี้ในธรรมชาติเกิดขึ้นจาก ปฏิกิริยาของแสงกับก๊าซไนโตรเจนที่เป็นส่วนประกอบของบรรยากาศ (Photo-oxidation) และยังพบได้ในสภาพแวดล้อมทั่วไปปัจจุบัน ก๊าซไนตรัสออกไซด์ในบรรยากาศโลกเพิ่มขึ้นในแต่ละปีประมาณ 7-13 ล้านตัน</p>
        <br>
        <p><b>ก๊าซซีเอฟซี</b> (CFC) หรือ คลอโรฟลูออโรคาร์บอน (Chlorofluorocarbon) เป็นก๊าซที่มีคุณสมบัติ ไม่ติดไฟและไม่มีพิษ ไม่ได้เกิดขึ้นในบรรยากาศตามธรรมชาติ แต่ถูกสังเคราะห์ขึ้นเพื่อใช้ใน กิจกรรมบางประเภท โดยเฉพาะในโรงงานอุตสาหกรรม ได้แก่ เครื่องทำความเย็น สเปรย์ น้ำยาดับเพลิงก๊าซชนิดนี้ส่งผลกระทบรุนแรงต่อบรรยากาศ ทั้งทำให้โลกร้อนขึ้นและทำลายบรรยากาศโลกจนเกิด รูรั่วในชั้นโอโซน ทำให้ รังสีอัลตราไวโอเลต สามารถส่องลงมาถึงพื้นโลกได้มากขึ้น ส่งผลกระทบต่อสิ่งมีชีวิตบนโลก เช่น เพิ่มความเสี่ยงในการเกิดโรคมะเร็งผิวหนังในมนุษย์ เป็นต้น</p>
        <br>
        <p><b>ก๊าซโอโซน</b> (Ozone: O₃) ส่วนใหญ่เกิดจากปฏิกิริยาทางธรรมชาติระหว่างแสงแดดกับออกซิเจน (O₂) และแสงแดดกับก๊าซไนตรัสออกไซด์โดยปกติแล้ว โอโซนในบรรยากาศชั้นสูงมีบทบาทสำคัญในการปกป้องโลกจากรังสีอัลตราไวโอเลตจากดวงอาทิตย์ แต่เมื่อโอโซนอยู่ใกล้พื้นผิวโลก จะกลายเป็นก๊าซพิษชนิดหนึ่ง ที่เกิดจากปฏิกิริยาระหว่างโอโซนกับแสงอาทิตย์ โดยโอโซนจะช่วยกระตุ้น ปฏิกิริยาโฟโต้ออกซิเดชัน (Photo-oxidation) ระหว่างแสงกับกลุ่มก๊าซไนโตรเจน ซึ่งส่งผลให้เกิด ก๊าซไนโตรเจนไดออกไซด์ (NO₂) ซึ่งเป็นก๊าซเรือนกระจกแม้ว่า ก๊าซโอโซนจะมีอยู่ในบรรยากาศเพียงเล็กน้อย แต่ก็สามารถดูดกลืนรังสีอินฟราเรดจากดวงอาทิตย์ และมีผลทำให้อุณหภูมิโลกสูงขึ้น หากปริมาณโอโซนในบรรยากาศเพิ่มขึ้น ก็จะเป็นปัจจัยที่ทำให้อุณหภูมิโลกสูงขึ้นไปด้วย</p>
        <br>
        <h5>กลุ่มกิจกรรมที่ทำให้เกิดสภาวะโลกร้อนในประเทศไทย</h5>
        <br>
        <p>1. กลุ่มกิจกรรมด้านพลังงาน ภาคพลังงานเป็นแหล่งปล่อยก๊าซเรือนกระจกอันดับหนึ่งของประเทศ โดยก๊าซเหล่านี้ถูกปล่อยจากกิจกรรมหลักและกิจกรรมย่อย ได้แก่</p>
        <p>1.1 การเผาผลาญพลังงาน กิจกรรมที่ใช้เชื้อเพลิงเพื่อผลิตพลังงาน ได้แก่ โรงงานผลิตกระแสไฟฟ้า โรงงานถลุงเหล็ก ซึ่งส่วนใหญ่ใช้เชื้อเพลิงประเภท ถ่านหินและน้ำมัน ส่งผลให้เกิดการปล่อย ก๊าซคาร์บอนไดออกไซด์ (CO₂) รวมถึงก๊าซอื่นๆ เช่น มีเทน (CH₄), ไนโตรเจนไดออกไซด์ (NO₂), และไนตรัสออกไซด์ (N₂O)</p>
        <p>1.2 การระเหยของก๊าซจากพลังงาน กระบวนการผลิตเชื้อเพลิง เช่น การทำเหมืองถ่านหิน ทำให้ ก๊าซมีเทน (CH₄) ระเหยออกมา รวมถึง การผลิต ขนส่ง และเก็บรักษาก๊าซและน้ำมัน ซึ่งส่งผลให้มีการปลดปล่อย ก๊าซมีเทน (CH₄) และสารอินทรีย์ระเหยง่ายที่ไม่ใช่มีเทน (Non-methane Volatile Organic Compounds: NMVOCs)</p>
        <br>
        <p>2. กลุ่มกิจกรรมด้านการเกษตรและป่าไม้กิจกรรมที่ก่อให้เกิดก๊าซเรือนกระจกส่วนใหญ่ เกิดจากการตัดไม้ทำลายป่า และ การเปลี่ยนแปลงการใช้ที่ดิน ซึ่งทำให้มีการปล่อย ก๊าซคาร์บอนไดออกไซด์ (CO₂) ออกสู่บรรยากาศนอกจากนี้ กิจกรรมด้านการเกษตร ก็เป็นอีกแหล่งสำคัญของก๊าซเรือนกระจก โดยเฉพาะ การทำนาข้าว ซึ่งจากการวิจัยพบว่า พื้นที่นาข้าวสามารถปล่อยก๊าซมีเทน (CH₄) ประมาณ 16-28 มิลลิกรัมต่อตารางเมตรต่อชั่วโมง</p>
        <br>
        <p>3. กลุ่มกิจกรรมด้านการผลิตทางอุตสาหกรรม</p>
        <p>กลุ่มกิจกรรมทางอุตสาหกรรม เป็นอีกแหล่งปล่อยก๊าซเรือนกระจกที่สำคัญ โดยเกิดจาก การใช้เชื้อเพลิงประเภทน้ำมันและถ่านหินในการผลิต รวมถึง การใช้สารซีเอฟซี (CFCs) ในเครื่องทำความเย็น ซึ่งทั้งสองปัจจัยนี้ส่งผลกระทบโดยตรงต่อภาวะโลกร้อน</p>
        <br>
        <h5>ผลกระทบจากสภาวะโลกร้อน</h5>
        <p>การที่อุณหภูมิของโลกสูงขึ้นส่งผลกระทบต่อสิ่งแวดล้อมในหลายด้าน ดังนี้</p>
        <br>
        <p><b>1. ระบบนิเวศ</b> การเปลี่ยนแปลงของอุณหภูมิและปริมาณน้ำฝนมีผลกระทบต่อพืชและสัตว์ในระบบนิเวศอย่างมาก นักวิทยาศาสตร์คาดว่า ภายในปี พ.ศ. 2593 สิ่งมีชีวิตที่ไม่สามารถปรับตัวเข้ากับการเปลี่ยนแปลงของสภาพภูมิอากาศอาจสูญพันธุ์ไปกว่าล้านชนิด</p>
        <br>
        <p><b>2. การเปลี่ยนแปลงของสภาพอากาศ</b> 2.1 อุณหภูมิของผิวโลกเพิ่มสูงขึ้น
        จากข้อมูลของโครงการสิ่งแวดล้อมแห่งสหประชาชาติ คาดการณ์ว่าอุณหภูมิโลกจะเพิ่มขึ้นเฉลี่ย 2-4 องศาเซลเซียส ส่งผลให้ระดับน้ำทะเลสูงขึ้นประมาณ 20-50 เซนติเมตรภายใน 10-50 ปีข้างหน้า นอกจากนี้ยังทำให้สภาพอากาศแปรปรวน เช่น การเกิดพายุรุนแรงและภาวะภัยแล้งในบางพื้นที่</p>
        <br>
        <p><b>2.2 ระดับน้ำทะเลเพิ่มสูงขึ้น</b> อุณหภูมิที่เพิ่มขึ้นทำให้น้ำทะเลอุ่นขึ้นและเกิดการขยายตัว ส่งผลให้ธารน้ำแข็งขั้วโลกละลายและทำให้ระดับน้ำทะเลสูงขึ้น นักวิทยาศาสตร์คาดการณ์ว่าภายในปี พ.ศ. 2593 ระดับน้ำทะเลอาจสูงขึ้นถึง 20 เซนติเมตร จากการวิเคราะห์ภาพถ่ายดาวเทียมในช่วงปี พ.ศ. 2522-2546 พบว่า ในช่วง 20 ปีที่ผ่านมา น้ำแข็งบริเวณมหาสมุทรอาร์กติกได้ละลายไปแล้วถึงร้อยละ 6.5 และในปี พ.ศ. 2548 นักวิทยาศาสตร์ยังพบว่า แผ่นน้ำแข็งในบริเวณนี้ได้ละลายหายไปมากขึ้นเป็นร้อยละ 8 นอกจากนี้ยังพบว่าตั้งแต่ปี พ.ศ. 2545 เป็นต้นมา แผ่นน้ำแข็งในบริเวณขั้วโลกเหนือได้ละลายเร็วกว่าวงจรปกติถึง 17 วันปรากฏการณ์แผ่นน้ำแข็งละลายนี้ได้เกิดขึ้นในประเทศกรีนแลนด์ ซึ่งตั้งอยู่บริเวณขั้วโลกเหนือ จะเห็นว่าแผ่นน้ำแข็งได้ละลายกลายเป็นธารน้ำที่ไหลอยู่บนผืนแผ่นน้ำแข็ง</p>
        <br>
        <p><b>2.3 การเปลี่ยนแปลงของหยาดน้ำฟ้า</b> เมื่อโลกร้อนขึ้น ปริมาณหยาดน้ำฟ้าที่ตกลงสู่พื้นโลกในแต่ละปีอาจเพิ่มขึ้น นั่นคือเมื่ออุณหภูมิของอากาศใกล้พื้นดินสูงขึ้น น้ำจะระเหยจากผิวโลกมากขึ้น ในที่สุดน้ำที่ระเหยขึ้นไปจะตกกลับสู่โลกในรูปของฝนและหิมะ พื้นที่บางแห่งของโลกจะได้รับปริมาณฝนหรือหิมะเพิ่มกว่าที่ได้รับในปัจจุบัน ในขณะที่บางแห่งจะได้รับน้อยลง</p>
        <br>
        <p><b>3. สิ่งมีชีวิตบนโลก</b> ผลกระทบจากสภาวะโลกร้อนที่มีต่อสิ่งมีชีวิตบนโลกการเปลี่ยนแปลงทางภูมิอากาศและอุณหภูมิที่สูงขึ้นส่งผลกระทบต่อสิ่งมีชีวิตต่างๆ เช่น การทำลายพันธุ์พืช และวิถีการดำรงชีวิตของสิ่งมีชีวิต</p> 
        <br>
        <b>3.1 การเปลี่ยนแปลงเขตพันธุ์พืช</b>
        <br>
        <p>พืชต่างชนิดกันต้องการสภาพอากาศที่แตกต่างกัน เมื่ออุณหภูมิของโลกเพิ่มขึ้นและภูมิอากาศเปลี่ยนไป เขตพันธุ์พืชที่มีอยู่ในโลกปัจจุบันจะเลื่อนไปจากบริเวณเขตร้อนหรือเขตศูนย์สูตรขึ้นไปยังบริเวณขั้วโลกเหนือและขั้วโลกใต้ นอกจากนี้ ฤดูกาลต่างๆ ก็จะคลาดเคลื่อนตามไปด้วย ส่งผลให้พืชพรรณบางชนิดที่มีการเจริญเติบโตช้า เช่น พันธุ์พืชเมืองหนาว ได้รับผลกระทบจากการเปลี่ยนแปลงนี้</p>
        <br>
        <b>3.2 การเสื่อมโทรมของปะการัง</b>
        <br>
        <p>ปะการังเป็นสิ่งมีชีวิตที่อาศัยแหล่งน้ำทะเลในการเจริญเติบโต และมีทั้งที่เป็นพืชและสัตว์ พบมากในทะเลน้ำลึก สำหรับประเทศไทย ปะการังอาศัยอยู่ทั้งฝั่งทะเลด้านอ่าวไทยและฝั่งทะเลด้านอันดามัน การเพิ่มขึ้นของอุณหภูมิทะเลทำให้เกิดปรากฏการณ์ "ปะการังฟอกขาว" ซึ่งคือการที่ปะการังมีสีซีดจางลงจากปกติ เป็นสีครีมหรือสีขาว ปะการังที่เสื่อมสภาพทำให้สิ่งมีชีวิตที่อาศัยในแนวปะการัง เช่น ปลาและสัตว์น้ำอื่นๆ ลดจำนวนลง</p>
        <br>
        <b>3.3 การเปลี่ยนแปลงด้านเกษตรกรรม</b>
        <p>การเปลี่ยนแปลงภูมิอากาศที่เกิดจากภาวะโลกร้อนส่งผลให้ผลผลิตการเกษตรในบางประเทศลดลงอย่างรวดเร็ว เกิดการขาดแคลนอาหาร ซึ่งเป็นอันตรายเร่งด่วนและรุนแรงจากผลกระทบของสภาวะโลกร้อน</p>
        <br>
        <b>3.4 สุขภาพอนามัยของมนุษย์</b>
        <p>การเพิ่มขึ้นของอุณหภูมิทำให้เกิดการแพร่ระบาดของโรคติดต่อทั้งโรคที่เคยสูญหายไปแล้วในอดีต และโรคอุบัติใหม่ การระบาดของโรคเหล่านี้แพร่กระจายไปยังพื้นที่ที่ไม่เคยมีการระบาดมาก่อน เพราะอุณหภูมิที่สูงขึ้นทำให้เกิดภาวะที่เหมาะสมสำหรับการเพาะพันธุ์และการเจริญเติบโตของเชื้อโรคและพาหะนำโรคต่างๆ</p>
        <br>
        <b>3.5 ผลกระทบด้านสังคมและการเมือง</b>
        <p>การปรับตัวให้เข้ากับการเปลี่ยนแปลงของภูมิอากาศจำเป็นต้องใช้ทรัพยากรจำนวนมาก ทำให้ทรัพยากรที่นำมาใช้ในการแก้ไขปัญหาอื่นๆ ที่จำเป็นมีน้อยลง ส่งผลให้รัฐบาลต้องเผชิญกับปัญหาการใช้จ่ายงบประมาณอย่างจำกัด ซึ่งอาจทำให้การพัฒนาประเทศเกิดความไม่เป็นธรรม เมื่อมาตรฐานชีวิตของประชาชนถดถอยลง อาจนำไปสู่ความไม่มั่นคงทางสังคม และอาจเกิดความรุนแรงทางการเมืองตามมา</p>
        <br>
        <h5>การป้องกันแก้ไขผลกระทบจากสภาวะโลกร้อน</h5>
        <br>
        <h5>การมีส่วนร่วมของประชาชน</h5>
        <p>การป้องกันและแก้ไขปัญหาผลกระทบจากสภาวะโลกร้อน เป็นปัญหาระดับโลกที่ทุกชาติให้ความสำคัญในการดำเนินการอย่างมาก สรุปแนวทางป้องกันและแก้ไขดังนี้</p>
        <br>
        <p><b>การลดปริมาณการปล่อยก๊าซเรือนกระจก</b> เนื่องจากก๊าซเรือนกระจกเป็นสาเหตุสำคัญที่ทำให้เกิดสภาวะโลกร้อน ดังนั้นการลดปริมาณก๊าซเรือนกระจกต้องอาศัยความร่วมมือจากองค์กรหรือหน่วยงานหลายฝ่าย จากการเจรจาในระดับนานาชาติ ตั้งแต่ปี พ.ศ. 2533 ได้มีการจัดทำพิธีสารเกียวโต (The Kyoto Protocol) เพื่อแก้ไขอุณหภูมิโลกที่สูงขึ้น โดยในปี พ.ศ. 2540 กำหนดเป้าหมายในการลดมลพิษจากปี พ.ศ. 2533 ให้ลดลงอีกโดยเฉลี่ยร้อยละ 5.2 ซึ่งแนวทางในการลดปริมาณก๊าซเรือนกระจกที่สามารถปฏิบัติได้เป็นรูปธรรมมีดังนี้</p>
        <br>
        <p>1.1 การลดการตัดไม้ทำลายป่า และส่งเสริมการปลูกต้นไม้ เพื่อให้ต้นไม้ช่วยดูดก๊าซ CO₂ ในกระบวนการสังเคราะห์แสง และเมื่อมีการโค่นและเผาทำลายป่าไม้ กิจกรรมดังกล่าวจะทำให้ก๊าซที่สะสมอยู่ในต้นไม้ถูกปล่อยกลับสู่สิ่งแวดล้อม</p>
        <br>
        <p>1.2 การกำหนดมาตรฐานการปล่อยก๊าซคาร์บอนไดออกไซด์ และก๊าซเรือนกระจกอื่น ๆ จากแหล่งกำเนิด โดยเฉพาะจากการผลิตกระแสไฟฟ้าและจากการเผาไหม้เชื้อเพลิงของยานยนต์</p>
        <br>
        <p>1.3 การลดปริมาณก๊าซมีเทน โดยการลดการบริโภคที่ฟุ่มเฟือย รวมทั้งลดการใช้ทรัพยากรที่อาจนำไปสู่การเน่าเปื่อยผุพัง ซึ่งเป็นสาเหตุในการเกิดก๊าซมีเทน</p>
        <br>
        <p>1.4 การต่อต้านการใช้สารซีเอฟซี (CFCs) ควรมีการลด ละ หรือเลิกใช้วัสดุที่มีส่วนประกอบของสารซีเอฟซี เช่น โฟมบรรจุอาหาร สเปรย์น้ำหอมปรับอากาศ เป็นต้น</p>
        <br>
        <p><b>2. การอนุรักษ์พลังงาน</b> ในสนธิสัญญาเกียวโตได้กำหนดแนวทางในการลดมลพิษที่ก่อให้เกิดสภาวะโลกร้อน โดยให้เพิ่มเงินลงทุนอุดหนุนเพื่อลดการใช้พลังงานจากเชื้อเพลิงฟอสซิล และกำหนดให้เลือกใช้พลังงานทดแทนจากธรรมชาติ เช่น พลังงานลม พลังงานแสงอาทิตย์ เป็นต้น สำหรับแนวทางที่จะนำไปสู่การปฏิบัติให้เกิดผลเป็นรูปธรรมมีดังนี้</p>
        <br>
        <p>2.1 การประหยัดพลังงานเชื้อเพลิง การใช้เชื้อเพลิงอย่างมีประสิทธิภาพเป็นแนวทางหนึ่งที่ช่วยลดการปล่อยก๊าซคาร์บอนไดออกไซด์ในชั้นบรรยากาศ</p>
        <br>
        <p>2.2 การเลือกการเดินทางที่เหมาะสม การเดินทางด้วยรถยนต์เป็นการเพิ่มก๊าซคาร์บอนไดออกไซด์ในบรรยากาศ และยังทำให้เกิดก๊าซโอโซนและมีเทนเพิ่มขึ้นด้วย ดังนั้นหากเป็นการเดินทางระยะใกล้หรือไม่ไกลมาก ควรเดินหรือใช้รถจักรยานแทน</p>
        <br>
        <p>การใช้พลังงานทดแทน การมีการนำพลังงานทดแทนที่เกิดจากธรรมชาติ เช่นพลังงานน้ำ พลังลม มาใช้ให้มากขึ้น เพราะพลังงานเหล่านี้ไม่ก่อให้เกิดกาสเลือนกระจก</p>
        <br>
        <p>2.3 การลดการใช้ปุ๋ย ควรลดปริมาณการใช้ปุ๋ยไนโตรเจน ลดการเผาไหม้เชื้อเพลิงประเภทถ่านและถ่านหิน ลดปริมาณปศุสัตว์เพื่อลดปริมาณของเสียจากสัตว์ เพื่อช่วยลดก๊าซไนโตรเจนออกไซด์ในบรรยากาศ</p>
        <br>
        <p>3. การรักษา เสริมสร้าง และเพิ่มพูนแหล่งดูดซับและแหล่งกักเก็บก๊าซเรือนกระจก ด้วยการรักษาพื้นที่ป่า โดยการลดการตัดไม้ทำลายป่า และการปลูกป่า</p>
        <br>
        <h5>การมีส่วนร่วมในระดับนานาชาติ</h5>
        <p>อนุสัญญาว่าด้วยการเปลี่ยนแปลงสภาพภูมิอากาศและพิธีสารว่าด้วยการเปลี่ยนแปลงสภาพภูมิอากาศของสหประชาชาติ เกิดขึ้นมาเป็นลำดับตั้งแต่ปี พ.ศ. 2528 จนถึงปัจจุบัน ดังนี้</p>
        <br>
        <p><b>1. อนุสัญญาเวียนนา</b> (Vienna Convention) เป็นอนุสัญญาว่าด้วยการป้องกันบรรยากาศชั้นโอโซน จัดทำขึ้นโดยโครงการสิ่งแวดล้อมแห่งสหประชาชาติ (UNEP) เมื่อวันที่ 18-22 มีนาคม 2528 ณ กรุงเวียนนา ประเทศออสเตรีย</p>
        <br>
        <p><b>2. พิธีสารมอนทรีออล</b> (Montreal Protocol) เป็นข้อตกลงระดับรองที่ต่อเนื่องจากอนุสัญญาเวียนนา จัดทำขึ้นเมื่อวันที่ 15 กันยายน 2531 โดยมีจุดประสงค์เพื่อวางแผนดำเนินการร่วมกันของประเทศสมาชิก เพื่อกำหนดมาตรการในการควบคุมการผลิต การใช้ และการค้าสารเคมีที่ทำลายชั้นโอโซนในบรรยากาศ โดยให้มีการควบคุมในระดับอุตสาหกรรม ประเทศไทยได้เข้าร่วมลงนามเป็นสมาชิกของอนุสัญญาเวียนนาและพิธีสารมอนทรีออลเมื่อวันที่ 7 กรกฎาคม 2532 มีผลให้ประเทศไทยต้องควบคุมปริมาณการผลิตและการใช้สารดังกล่าวตั้งแต่วันที่ 5 ตุลาคม 2532 เป็นต้นมา</p>
        <br>
        <p><b>3. การประชุม Earth Summit</b> ว่าด้วยสิ่งแวดล้อมและการพัฒนาที่ประเทศบราซิลในเดือนมิถุนายน 2535 ได้มีการลงนามและรับรองเอกสารที่สำคัญ 5 ฉบับ ได้แก่</p>
        <br>
        <p>3.1 ปฏิญญาริโอว่าด้วยสิ่งแวดล้อมและการพัฒนา เกี่ยวกับสิทธิและความรับผิดชอบของประชาชาติในการดำเนินงานพัฒนาเพื่อปรับปรุงความเป็นอยู่ของประชาชนให้ดียิ่งขึ้น</p>
        <br>
        <p>3.2 แผนปฏิบัติการ 21 (Agenda 21) เป็นแผนแม่บทของโลกสำหรับการดำเนินงานที่จะทำให้เกิดการพัฒนาอย่างยั่งยืนทั้งในด้านสังคม เศรษฐกิจ และสิ่งแวดล้อม</p>
        <br>
        <p>3.3 คำแถลงเกี่ยวกับหลักการในเรื่องป่าไม้ เพื่อเป็นแนวทางในการจัดการ อนุรักษ์ และพัฒนาอย่างยั่งยืนสำหรับป่าไม้ทุกประเภท</p>
        <br>
        <p>กรอบอนุสัญญาสหประชาชาติว่าด้วยการเปลี่ยนแปลงสภาพภูมิอากาศ มีวัตถุประสงค์เพื่อรักษาระดับก๊าซเรือนกระจกในบรรยากาศ โดยลดปริมาณการปล่อยก๊าซเรือนกระจกขึ้นสู่ชั้นบรรยากาศ</p>
        <br>
        <p>3.4  อนุสัญญาว่าด้วยความหลากหลายทางชีวภาพ เพื่อให้ยอมรับวิธีการอนุรักษ์ความหลากหลายของชนิดพันธุ์สิ่งมีชีวิต และเกิดการแบ่งปันผลตอบแทนอย่างเป็นธรรมและเท่าเทียมกัน</p>
        <br>
        <h5>4. พิธีสารเกียวโต (The Kyoto Protocol) พ.ศ. 2540 กำหนดพันธกรณี ดังนี้</h5>
        <br>
        <p>4.1 พันธกรณีที่ทุกประเทศต้องดำเนินการ คือการจัดทำบัญชีแห่งชาติว่าด้วยปริมาณการปล่อยก๊าซเรือนกระจกที่เกิดจากกิจกรรมของมนุษย์ และติดตามผลกระทบที่เกิดขึ้น</p>
        <br>
        <p>4.2 ประเทศที่พัฒนาแล้ว ต้องลดปริมาณการปล่อยก๊าซเรือนกระจกลงโดยเฉลี่ยร้อยละ 5.2 จากปริมาณที่ปล่อยในปี พ.ศ. 2533 ให้ได้ภายในปี พ.ศ. 2551-2555</p>
        <br><br>
        <b>นโยบายและกลไกในการลดการปล่อยก๊าซเรือนกระจก</b>
        <br>
        <p>1. เจไอ (Joint Implementation: JI) เป็นการดำเนินโครงการร่วมกันระหว่างประเทศที่พัฒนาแล้ว เพื่อช่วยลดปริมาณการปล่อยก๊าซเรือนกระจก</p>
        <p>2. อีที (Emission Trading: ET) เป็นการซื้อขายเครดิตการลดปริมาณการปล่อยก๊าซเรือนกระจกระหว่างกลุ่มประเทศพัฒนาแล้ว</p>
        <p>3. ซีดีเอ็ม (Clean Development Mechanism: CDM) เป็นการดำเนินโครงการร่วมกันระหว่างประเทศพัฒนาแล้วกับประเทศกำลังพัฒนา โดยมีวัตถุประสงค์เพื่อช่วยประเทศกำลังพัฒนาให้บรรลุถึงการพัฒนาที่ยั่งยืน และช่วยประเทศพัฒนาแล้วให้สามารถดำเนินการตามพันธกรณีที่กำหนดไว้ในพิธีสารเกียวโต</p>
        <br><br>
        <p>จากการประชุมรัฐภาคีอนุสัญญาสหประชาชาติว่าด้วยการเปลี่ยนแปลงสภาพภูมิอากาศสมัยที่ 24 หรือ Conference of the Parties (COP24) ณ เมืองคาโตวีตเซ สาธารณรัฐโปแลนด์ ระหว่างวันที่ 2-15 ธันวาคม 2561 การประชุมครั้งนี้เป็นการเจรจาระหว่างผู้นำประเทศต่างๆ โดยวาระสำคัญคือการกำหนดกฎกติกาใหม่เพื่อให้ทุกประเทศนำไปปฏิบัติได้จริง เป้าหมายหลักคือจำกัดการเพิ่มขึ้นของอุณหภูมิไม่ให้เกิน 1.5-2 องศาเซลเซียส เมื่อเทียบกับยุคก่อนปฏิวัติอุตสาหกรรม ซึ่งจะมีผลบังคับใช้อย่างเป็นทางการในปี 2563 นอกจากนี้ ยังได้กำหนดหลักเกณฑ์ใหม่ที่อนุญาตให้มีกฎที่แตกต่างกันระหว่างประเทศพัฒนาแล้วกับประเทศกำลังพัฒนา โดยประเทศกำลังพัฒนาจะได้รับการสนับสนุนเงินทุนและมีกลไกติดตามผลที่ยืดหยุ่นกว่าประเทศพัฒนาแล้ว</p>
        <br><br>
        <b>สรุป</b>
        <p>สภาพภูมิอากาศที่แปรปรวนไปทั่วโลก ทั้งฝนตกหนัก อากาศหนาวเย็น พายุหิมะ และฤดูแล้งที่ยาวนาน ล้วนเป็นปัญหาที่เกิดจาก สภาวะโลกร้อน เนื่องจากอุณหภูมิโลกที่สูงขึ้น ซึ่งส่งผลกระทบต่อมนุษย์และสิ่งมีชีวิตอื่นๆ รวมถึงทำให้ระบบนิเวศเปลี่ยนแปลง

ปัญหาดังกล่าวเกิดจากกิจกรรมของมนุษย์ที่ทำให้ ก๊าซเรือนกระจก เพิ่มขึ้น แนวทางป้องกันและแก้ไขปัญหาคือการร่วมมือกันของประชาชน ภาครัฐ และเอกชน ในการลดปริมาณก๊าซเรือนกระจก การอนุรักษ์พลังงาน และ การรักษาและเสริมสร้างแหล่งกักเก็บก๊าซเรือนกระจก โดยเฉพาะการรักษาพื้นที่ป่าไม้

นอกจากนี้ ยังต้องดำเนินงานร่วมกันในระดับนานาชาติเพื่อพิทักษ์สภาพภูมิอากาศ เช่น การประชุมระหว่างประเทศเกี่ยวกับการเปลี่ยนแปลงสภาพภูมิอากาศ เพื่อกระตุ้นให้เห็นความสำคัญ และการจัดทำเอกสารข้อตกลงระดับนานาชาติ เพื่อให้เกิดความร่วมมือในการลดผลกระทบจากการเปลี่ยนแปลงสภาพภูมิอากาศ</p>
